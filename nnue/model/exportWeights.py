def rotate(arr):
    newArr = np.zeros(dtype=float, shape=(arr.shape[1], arr.shape[0]))

    for x in range(arr.shape[0]):
        for y in range(arr.shape[1]):
            newArr[y][x] = arr[x][y]

    return newArr

def exportWeights(path, model):

    outfile = open(path, "wb")

    model = tf.keras.models.load_model(model)

    weights = model.get_weights()

    for i in range(len(weights)):
        if len(weights[i].shape) == 2:
            weights[i] = rotate(weights[i])

    for arr in weights:
        print(arr.shape)

        for weight in arr.flatten():
            outfile.write(struct.pack('>f', weight))

