static inline unsigned int get_feature_index(unsigned int pt, unsigned int sq){
    return (prptype[pt] * 64) + prsq[sq];
}

void input(){
    memset(input_layer, 0, sizeof input_layer);

    for (unsigned int ptype = P; ptype <= k; ptype++){
        U64 bitboard = bitboards[ptype];
        while (bitboard){
            unsigned int square = bsf(bitboard);

            input_layer[get_feature_index(ptype, square)] = 1;

            pop_bit(bitboard, square);
        }
    }
}

float nnue_evaluate() {

    input();
    full_propogate();

    return l4[0];
}

