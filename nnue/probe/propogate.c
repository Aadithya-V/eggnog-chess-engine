unsigned int LAYER_SIZES[] = {768, 768, 256, 32, 1};

float neuron(unsigned int layer, const float leading_neurons[], const float weights[], float bias){
    unsigned int size = LAYER_SIZES[layer-1];

    for (int i = 0; i < size; ++i) {
        bias += leading_neurons[i] * weights[i];
    }

    return bias;
}

void full_propogate(){
    for (int i = 0; i < 768; i++){
        l1[i] = RELU(neuron(1, input_layer, l1w[i], l1b[i]));
    }

    for (int i = 0; i < 256; i++){
        l2[i] = RELU(neuron(2, l1, l2w[i], l2b[i]));
    }

    for (int i = 0; i < 32; i++){
        l3[i] = RELU(neuron(3, l2, l3w[i], l3b[i]));
    }

    l4[0] = neuron(4, l3, l4w[0], l4b[0]);
}

